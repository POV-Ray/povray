// -----------------------------------------------------------------------
// --------- AFFICHE UNE ICONE -------------------------------------------
// -----------------------------------------------------------------------
void affiche_icone(int X,int Y,byte Mode,char *Icone) {
  char *Image;
  int Xi,Yi;
  int L,H,i,j,C;

  if (NbCouleurs==256 && !Mode) {
    Xi=Icone[0]+256*Icone[1];
    Yi=Icone[2]+256*Icone[3];

    Image=(char *) mem_alloc((Xi*Yi)+6);
    memcpy(Image,Icone,4);
    memcpy(Image+6,Icone+4,Xi*Yi);

    #if !WINDOWS
    _putimage(X,Y,Image,_GPSET);
    #endif

    mem_free(Image,6+(Xi*Yi));
  } else {
    L=Icone[0];
    H=Icone[2];

    for (i=0;i<H;i++) {
      for (j=0;j<L;j++) {
        C=Icone[4+i*L+j];
        if (C!=7) put_pixel(X+j,Y+i,C);
      }
    }
  }
}

